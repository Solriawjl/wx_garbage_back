def seed_data():
    db = SessionLocal()
    try:
        added_count = 0
        for item_data in knowledge_items:
            # 检查数据库里是不是已经存在这个词了，防止重复插入
            existing_item = db.query(models.GarbageItem).filter(
                models.GarbageItem.item_name == item_data["name"]
            ).first()

            if not existing_item:
                new_item = models.GarbageItem(
                    item_name=item_data["name"],
                    category_type=item_data["cid"],
                    tips=item_data["tips"],
                    image_url=""  # 使用默认图片
                )
                db.add(new_item)
                added_count += 1

        db.commit()
        print(f"🎉 知识库初始化成功！共为你添加了 {added_count} 个全新品类！")
        if added_count == 0:
            print("💡 词库里的词都已经是满的啦，无需重复添加。")

    except Exception as e:
        print(f"❌ 导入失败: {e}")
        db.rollback()
    finally:
        db.close()


if __name__ == "__main__":
    seed_data()